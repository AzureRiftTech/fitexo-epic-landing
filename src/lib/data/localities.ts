// Get unique cities for city-level pages
export function getUniqueCities(): string[] {
    const allLocalities = getAllLocalities();
    return [...new Set(allLocalities.map(l => l.city))];
}

// Get localities by city
export function getLocalitiesByCity(city: string): Locality[] {
    return getAllLocalities().filter(l => l.city.toLowerCase() === city.toLowerCase());
}

// Get locality by slug
export function getLocalityBySlug(slug: string): Locality | undefined {
    return getAllLocalities().find(l => l.slug === slug);
}
